using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class SampleScript
{
    public int Add(int a, int b)
    {
        return a + b;
    }

    public int Sub(int a, int b)
    {
        return a - b;
    }

    public int Multi(int a, int b)
    {
        return a * b;
    }
}